static int cx = 0, cy = 0;
static uint32_t fg = 0xFFFFFFFF;

void cd32_printf(const char *fmt, ...)
{
    va_list args;
    va_start(args, fmt);
    char buf[256];
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);

    for (char *p = buf; *p; p++) {
        char c = *p;
        if (c == '\n') { cx = 0; cy += FONT_H; continue; }
        if (c == '\r') { cx = 0; continue; }
        if ((unsigned char)c >= 128) c = '?';

        if (cy + FONT_H > CD32_FB_H) {
            // Scroll: copy everything up by FONT_H lines
            // Simple reset for now
            cy = 0;
            cd32_video_clear(0x00000000);
        }

        for (int row = 0; row < FONT_H; row++) {
            uint8_t bits = font[(int)c][row];
            for (int col = 0; col < FONT_W; col++) {
                cd32_video_putpixel(cx + col, cy + row,
                    (bits & (0x80 >> col)) ? fg : 0);
            }
        }
        cx += FONT_W;
        if (cx + FONT_W > CD32_FB_W) { cx = 0; cy += FONT_H; }
    }
    cd32_video_kick();
}
