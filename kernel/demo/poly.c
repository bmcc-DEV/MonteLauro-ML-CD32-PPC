static int isin(int deg) {
    deg = deg % 1024;
    if (deg < 0) deg += 1024;
    return sin_table[deg];
}

static int icos(int deg) {
    return isin(deg + 256);
}

static void draw_pixel(int x, int y, uint32_t color) {
    cd32_video_putpixel(x, y, color);
}

static void draw_triangle(int x0, int y0, int x1, int y1, int x2, int y2, uint32_t color) {
    int minX = x0, maxX = x0;
    int minY = y0, maxY = y0;
    if (x1 < minX) minX = x1; if (x1 > maxX) maxX = x1;
    if (x2 < minX) minX = x2; if (x2 > maxX) maxX = x2;
    if (y1 < minY) minY = y1; if (y1 > maxY) maxY = y1;
    if (y2 < minY) minY = y2; if (y2 > maxY) maxY = y2;

    if (minX < 0) minX = 0; if (maxX >= CD32_FB_W) maxX = CD32_FB_W - 1;
    if (minY < 0) minY = 0; if (maxY >= CD32_FB_H) maxY = CD32_FB_H - 1;

    int sa = (x1 - x0) * (y2 - y0) - (x2 - x0) * (y1 - y0);

    for (int y = minY; y <= maxY; y++) {
        for (int x = minX; x <= maxX; x++) {
            int w0 = (x1 - x0) * (y - y0) - (y1 - y0) * (x - x0);
            int w1 = (x2 - x1) * (y - y1) - (y2 - y1) * (x - x1);
            int w2 = (x0 - x2) * (y - y2) - (y0 - y2) * (x - x2);

            if (sa >= 0) {
                if (w0 >= 0 && w1 >= 0 && w2 >= 0)
                    draw_pixel(x, y, color);
            } else {
                if (w0 <= 0 && w1 <= 0 && w2 <= 0)
                    draw_pixel(x, y, color);
            }
        }
    }
}

void game_main(void) {
    cd32_video_init();
    cd32_input_init();

    while (1) {
        cd32_input_poll();
        cd32_video_clear(0x00102030);

        int cx = CD32_FB_W / 2;
        int cy = CD32_FB_H / 2;
        int r = 180;
        int a = frame * 3;

        int p1x = cx + (r * icos(a) / 290);
        int p1y = cy + (r * isin(a) / 290);
        int p2x = cx + (r * icos(a + 341) / 290);
        int p2y = cy + (r * isin(a + 341) / 290);
        int p3x = cx + (r * icos(a + 682) / 290);
        int p3y = cy + (r * isin(a + 682) / 290);

        int r2 = 60;
        int q1x = cx + (r2 * icos(-a) / 290);
        int q1y = cy + (r2 * isin(-a) / 290);
        int q2x = cx + (r2 * icos(-a + 341) / 290);
        int q2y = cy + (r2 * isin(-a + 341) / 290);
        int q3x = cx + (r2 * icos(-a + 682) / 290);
        int q3y = cy + (r2 * isin(-a + 682) / 290);

        draw_triangle(p1x, p1y, p2x, p2y, p3x, p3y, 0x004060FF);
        draw_triangle(q1x, q1y, q2x, q2y, q3x, q3y, 0x00FF6040);

        cd32_video_kick();
        cd32_video_wait_vblank();
        frame++;
        if (frame > 9999) frame = 0;
    }
}
