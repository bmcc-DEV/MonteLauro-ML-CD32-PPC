static int isin(int d) { d %= 1024; if (d < 0) d += 1024; return sin_table[d]; }
static int icos(int d) { return isin(d + 256); }

/* Project 3D point to 2D screen */
static void project(int x3d, int y3d, int z3d, int rot, int *sx, int *sy)
{
    int rx = icos(rot) * x3d / 290 - isin(rot) * z3d / 290;
    int rz = isin(rot) * x3d / 290 + icos(rot) * z3d / 290;
    int cx = CD32_FB_W / 2;
    int cy = CD32_FB_H / 2;
    int scale = 300;
    *sx = cx + rx * scale / (rz + 400);
    *sy = cy + y3d * scale / (rz + 400);
}

void game_main(void)
{
    cd32_video_init();
    cd32_input_init();

    /* Cube vertices */
    int verts[8][3] = {
        {-50,-50, 50}, { 50,-50, 50}, { 50, 50, 50}, {-50, 50, 50},
        {-50,-50,-50}, { 50,-50,-50}, { 50, 50,-50}, {-50, 50,-50}
    };
    int faces[6][5] = {
        {0,1,2,3, 0xFF4060FF}, /* front  - blue   */
        {1,5,6,2, 0xFF60FF40}, /* right  - green  */
        {5,4,7,6, 0xFFFF4060}, /* back   - red    */
        {4,0,3,7, 0xFFFFA040}, /* left   - orange */
        {3,2,6,7, 0xFFFF40FF}, /* top    - purple */
        {4,5,1,0, 0xFF40FFFF}  /* bottom - cyan   */
    };

    while (1) {
        cd32_input_poll();
        cd32_gpu_clear(0x102030);

        int rot = frame * 2;
        int sx[8], sy[8];
        for (int i = 0; i < 8; i++)
            project(verts[i][0], verts[i][1], verts[i][2], rot, &sx[i], &sy[i]);

        for (int f = 0; f < 6; f++) {
            cd32_gpu_triangle(sx[faces[f][0]], sy[faces[f][0]],
                              sx[faces[f][1]], sy[faces[f][1]],
                              sx[faces[f][2]], sy[faces[f][2]],
                              faces[f][4]);
            cd32_gpu_triangle(sx[faces[f][0]], sy[faces[f][0]],
                              sx[faces[f][2]], sy[faces[f][2]],
                              sx[faces[f][3]], sy[faces[f][3]],
                              faces[f][4]);
            cd32_gpu_line(sx[faces[f][0]], sy[faces[f][0]],
                          sx[faces[f][1]], sy[faces[f][1]], 0xFFFFFFFF);
            cd32_gpu_line(sx[faces[f][1]], sy[faces[f][1]],
                          sx[faces[f][2]], sy[faces[f][2]], 0xFFFFFFFF);
            cd32_gpu_line(sx[faces[f][2]], sy[faces[f][2]],
                          sx[faces[f][3]], sy[faces[f][3]], 0xFFFFFFFF);
            cd32_gpu_line(sx[faces[f][3]], sy[faces[f][3]],
                          sx[faces[f][0]], sy[faces[f][0]], 0xFFFFFFFF);
        }

        cd32_printf("Cube 3D - Frame %d\n", frame);
        cd32_gpu_present();
        frame++;
        if (frame > 9999) frame = 0;
    }
}
