/*
 * CDG2 CDG2 BSP — console.c
 * Driver framebuffer para GPU Lisa II (TBDR).
 *
 * Implementa uma interface mínima de console:
 *   - Framebuffer linear em VRAM (640x480, RGBA32)
 *   - Tile-based kick via GPU_CTRL
 *   - Font bitmap 8x16 embutida
 */

#include <exec/types.h>
#include <string.h>

#include "board.h"

#define FB_WIDTH    640
#define FB_HEIGHT   480
#define FB_STRIDE   (FB_WIDTH * 4)  /* RGBA32 = 4 bytes/pixel */
#define FONT_W      8
#define FONT_H      16

/* ── Framebuffer helpers ────────────────────────────────────────── */

static uint8_t * const fb = (uint8_t*)ML_VRAM_BASE;

static void put_pixel(int x, int y, uint32_t color)
{
    if (x < 0 || x >= FB_WIDTH || y < 0 || y >= FB_HEIGHT) return;
    volatile uint32_t *p = (volatile uint32_t*)(fb + y * FB_STRIDE + x * 4);
    *p = color;
}

static void fill_rect(int x, int y, int w, int h, uint32_t color)
{
    for (int row = 0; row < h; row++)
        for (int col = 0; col < w; col++)
            put_pixel(x + col, y + row, color);
}

static void kick_gpu(void)
{
    ML_GPU_LIST_ADDR = ML_VRAM_BASE;
    ML_GPU_CTRL = ML_GPU_CTRL_KICK;
}

